class Reamur :
    def suhu(x):
        cel =  x / 4/5
        fah = (x * 9/5)+32
        kel = x + 273
        print("suhu dalam celcius: ", cel)
        print("suhu dalam fahrenheit: ", fah)
        print("suhu dalam kelvin: ", kel)

    a = int(input("Masukan suhu dalam Reamur: "))
    suhu (a)  

