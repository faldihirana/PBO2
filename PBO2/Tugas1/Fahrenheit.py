class Fahrenheit :
    def suhu(x):
        rea = x * 4/5
        cel = (x * 9/5)-32
        kel = x + 273
        print("suhu dalam reamur: ", rea)
        print("suhu dalam celcius: ", cel)
        print("suhu dalam kelvin: ", kel)

    a = int(input("Masukan suhu dalam Fahrenheit: "))
    suhu (a)  

