class Kelvin :
    def suhu(x):
        rea = x * 4/5
        fah = (x * 9/5)+32
        cel = x - 273
        print("suhu dalam reamur: ", rea)
        print("suhu dalam fahrenheit: ", fah)
        print("suhu dalam kelvin: ", cel)

    a = int(input("Masukan suhu dalam Kelvin: "))
    suhu (a)  

